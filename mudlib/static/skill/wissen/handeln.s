Deine Faulheit besiegt Dich immer.
Bisher lagerst Du Deine Haendchen immer in den Hosentaschen.
Messer, Schere, Licht sind fuer Dich noch nicht.
Du faengst an, Dich fuer Deine Umwelt zu interessieren.
Ohne Messer brauchst Du das Brot nicht zu schneiden.
Mittlerweile merkst Du, dass Deine Haende nicht nur zum Kaempfen taugen.
Manches, was Du anpackst, geht noch schief. Aber nicht alles.
Du bist manchmal schon sehr einfallsreich. Nicht immer, aber immer oefter.
Ein Versuch ist Dir vieles wert, sogar einen Reinfall.
Wo Dein Wille erwacht, da findest Du auch einen Weg.
Du hast schon einige lustige Dinge angestellt.
Du musst Deine Finger ueberall reinstecken.
Fuer Dich geht probieren ueber studieren.
Dir macht es Spass, alles auszuprobieren.
Du bekommst alles kaputt, was Du in die Finger kriegst.
Du Fleissiges hast immer etwas zu tun.
Die Taetigkeit ist das Salz Deines Lebens.
Du wagst und gewinnst.
Was Du nicht schon alles angestellt hast.
Du schmiedest das Eisen, solange es heiss ist.
Kein Troll kann deinen Tatendrang bremsen.
Alle Raeder stehen still, wenn Dein starker Arm es will.
Du hast schon viel erlebt.
Du bist das Bluemchenpflueckende vor dem Herrn.
Du bist beruechtigt fuer Deine Taten.
