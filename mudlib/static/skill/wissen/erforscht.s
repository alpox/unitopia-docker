Du hast Magyra gerade erst entdeckt.
Ob Du auch abseits der Wege gehen kannst?
Sehr entdeckungslustig warst Du aber noch nicht.
Du hast schon Geruechte vernommen, es gaebe geheime Orte.
Du verwechselst noch Dabeisein mit Erleben.
Du bist nichts. Was Du suchst ist alles.
Du hast schon ein wenig von deiner Umgebung erforscht.
Du weisst schon, dass es geheime Orte gibt.
Dir, dem Mutigen, gehoert die Welt.
Du suchst gerne nach Verborgenem.
Die schoenste Freude erlebst Du immer da, wo Du sie am wenigsten erwartest.
Keine Entdeckung ist vor Dir sicher.
Du besuchst auch ungewoehnliche Orte.
Dir bleibt nicht viel in Magyra verborgen.
Du findest auch die hintersten Winkel am Ende der Welt.
Du hast Orte erforscht, die nicht vielen zu Augen gekommen sind.
Magyra kennst Du wie deine Hosentasche.
Du findest auch hinter dem hintersten Winkel am Ende der Welt noch etwas Neues.
Du warst schon fast ueberall.
Du stehst Christoph Kolumbus in nichts nach.
Was Du erlebt hast, kann Dir kein Gott mehr rauben.
