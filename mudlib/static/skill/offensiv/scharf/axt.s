AXT? Ist das ein Zaubergegenstand?
Ob man mit einer Axt eine Grube schaufeln kann?
Ach, Du weisst schon, dass eine Axt scharf sein kann.
Du hast ja sogar schon mal eine Axt in der Hand gehabt.
Mit viel Glueck haeltst du eine Axt am richtigen Ende fest.
Du kannst auf Anhieb das schwere Ende einer Axt bestimmen.
Auch ein blinder Axttraeger trifft mal einen Baum.
Noch ein bisschen Uebung, und Du triffst einen breiten Baum mit der Axt.
Durch die Uebung kannst Du einen Baum mit Deiner Axt treffen.
Dein Handgriff um Deine Axt ist fest und vertraut.
Schon ohne Zaudern triffst Du einen kleinen Baum mit der Axt.
Du triffst zwar einen Baum, kannst ihn aber immer noch nicht faellen.
Mit grosser Kraftanstrengung ist Dir auch das Baumfaellen moeglich.
Du hast schon gehoert, dass man eine Axt auch als Waffe nutzen kann.
Dank Deiner Baumfaellererfahrung gehst Du sicher mit der Axt um.
Die Baumfaellererfahrung erwartet leider ein ruhiges Opfer.
Du kannst jetzt schon mit der Axt den Kopf einer Schildkroete abspalten.
Mit einiger Muehe kannst Du sogar einer Ratte den Schwanz abhacken.
Bald wirst du den Umgang mit der Axt richtig koennen.
Morgen wirst Du erste richtige Erfolge mit Deiner Axt feiern koennen.
Schon spaltest Du mit Deiner Axt einen Orkschaedel mit einem Schlag.
Mit wachsender Staerke gewinnst Du endlich auch das Axtselbstvertrauen.
Mit gekonnter Axtpose kannst Du Dich bald jedem Gegner stellen.
Du darfst Dich jetzt Meister im Axtschlag nennen!
